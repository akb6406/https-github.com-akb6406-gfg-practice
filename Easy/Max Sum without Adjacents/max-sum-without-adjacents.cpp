class Solution{
public:	
	// calculate the maximum sum with out adjacent
	int findMaxSum(int *arr, int n) {
	    // code here
	     int curr = 0,prev = 0,next = 0 ;
	    for(int i = n-1 ; i>=0 ; i--){
	       prev = max(arr[i] + next  ,curr);
	       next = curr;
	       curr = prev;
	    }
	    return prev;
	}
};

