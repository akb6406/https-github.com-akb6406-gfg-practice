class Solution
{
    //Function to delete middle element of a stack.
    public void deleteMid(Stack<Integer>s,int sizeOfStack){
        // code here
        ArrayList<Integer> ar= new ArrayList<>();
        int l= sizeOfStack;
        while(l!=0){
            ar.add(s.pop());
            l--;
        }
        l= sizeOfStack;
        ar.remove((sizeOfStack)/2);
        int r= ar.size()-1;
        while(r>=0){
            s.push(ar.get(r));
            r--;
        }
    } 
}

